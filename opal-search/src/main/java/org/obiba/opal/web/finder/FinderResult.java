package org.obiba.opal.web.finder;

/**
 *
 */
public class FinderResult<T> {

  private T value;

  public FinderResult() {
  }

  public FinderResult(T value) {
    this.value = value;
  }

  public T getValue() {
    return value;
  }

  public void setValue(T value) {
    this.value = value;
  }
}
